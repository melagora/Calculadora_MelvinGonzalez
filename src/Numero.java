/**
 * La clase Numero representa un número con un valor de tipo double.
 * Proporciona métodos para obtener y establecer el valor del número.
 */

public class Numero {
    private double valor;

    public Numero(double valor) {
        this.valor = valor;
    }

    public double getValor() {
        return valor;
    }

    public void setValor(double valor) {
        this.valor = valor;
    }
}
